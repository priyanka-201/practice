public class VariableDemo {
    public static void main(String[] args) {
        int number;
        number=10;
        number=30;
        System.out.println(number);
        float cgpa=7.5f;
        System.out.println(cgpa);
        double weight =75.78;
        System.out.println("weight :"+weight);
        char vowel;
        vowel = 'a';
        vowel = 'e';
        System.out.println(vowel);
        int age=15;
        boolean isMajor;
        if(age>18) {
            isMajor=true;
        } else {
            isMajor=false;
        }

        System.out.println("Major :"+isMajor);
    }
}
